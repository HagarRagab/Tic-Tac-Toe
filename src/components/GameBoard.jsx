export default function GameBoard({ onSelectSquare, gameBoard }) {
    return (
        <ol id="game-board">
            {gameBoard.map((row, rowIndex) => (
                <li key={rowIndex}>
                    <ol>
                        {row.map((playerSymbol, colIndex) => (
                            <li key={colIndex}>
                                <button
                                    onClick={onSelectSquare.bind(
                                        null,
                                        rowIndex,
                                        colIndex
                                    )}
                                    disabled={playerSymbol}
                                >
                                    {playerSymbol}
                                </button>
                            </li>
                        ))}
                    </ol>
                </li>
            ))}
        </ol>
    );
}
